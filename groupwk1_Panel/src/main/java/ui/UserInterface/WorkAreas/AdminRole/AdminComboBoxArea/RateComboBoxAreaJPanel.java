    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel)rateTbl.getModel();
        model.setRowCount(0);
        for(Rate vs : rateList){
                Object[] row = new Object[4];
                row[0]=vs.getCourseId();
                row[1] = vs.getProfessorId();
                row[2] = vs.getStudentId();
                row[3] = vs.getScore();
                model.addRow(row);
        }
    }

